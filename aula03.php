    <h2>Tromba d'água impressiona banhistas no litoral do Paraná</h2>
    <img src="https://paranaportal.uol.com.br/wp-content/uploads/2020/02/tubarao-azul-guaratuba.jpg" alt="">
    <p>Uma tromba d'água registrada em Caiobá e no balneário Inajá, em Matinhos, no litoral do Paraná, impressionou banhistas </p>
    <p>Segundo especialistas da Somar Meteorologia, a tromba d'água é um tornado que se forma sobre algum corpo de água e está associado a correntes de ventos em rotação,
      em nuvens de tempestades intensas e de chuva.</p>
      <p>Os especialistas ressaltam que o fenômeno, no Brasil, é muito mais comum do que o tornado, principalmente nas regiões sul e sudeste. A grande maioria das trombas d'água,
        conforme a Somar, são de pequenas formações.</p>
